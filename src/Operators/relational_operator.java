package Operators;

public class relational_operator {

	public static void main(String[] args) {
		int a= 100;
		int b=10;
		
		System.out.println((a>b));//(100>50 ) true
		
		System.out.println((a<b));
		
		System.out.println((a<=b)); //(a>b)(a==b)(100<50)(100==50)
		System.out.println((a>=b));
		System.out.println((a==b));
		System.out.println((a!=b));
	}

}
